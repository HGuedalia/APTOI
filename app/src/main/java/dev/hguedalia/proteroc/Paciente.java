    public String toString() {
        int primeiroEspaco = nome.indexOf(" ");
        int segundoEspaco = nome.indexOf(" ", primeiroEspaco + 1);

        String nomeLista;
        if (segundoEspaco > primeiroEspaco) {
            nomeLista = nome.substring(0, segundoEspaco);
            int tamanhoCampo = 20;
            int caracteresNovoNome = nomeLista.length();
            int espacosFinal = tamanhoCampo - caracteresNovoNome;

            StringBuilder sb = new StringBuilder(nomeLista);
            for (int i = 0; i < espacosFinal; i++) {
                sb.append(" ");
            }
            nomeLista = sb.toString();
        } else {
            nomeLista = nome;
        }

        return leito + "   |   " + nomeLista + "   |   " + dataAtendimento;
    }
